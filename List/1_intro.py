# ------- List in Python ---------
# A built in data type that stores set of values
# It can store element of different types(integer,float,string,etc.)

marks = [45.2,55.3,65.2,99.5,22.6]
print(marks)
print(len(marks))           # print length of list
print(type(marks))          # print typeOf list
print(marks[0])             # print index vise
print(marks[1])

